import numpy as np

# Example
A = [[ -6, -1, -2, -1],
     [ -6,  2, -1,  1],
     [ -6, -8,  0, -1],
     [ 12,  8, -3, -2]]

b = [-44, 9, -3, -126]

[vrx, vry, rx, ry] = np.linalg.solve(A, b)

A = [[-6,  -8, -2,  0],
     [-6,  12, -1,  2],
     [-6,  -6,  0, -3],
     [12, -13, -3,  2]]

b = [-40, 16, -57, 7]

[vrz, vry, rz, ry] = np.linalg.solve(A, b)

print([vrx, vry, vrz])
print([rx, ry, rz])
print((sum([rx, ry, rz])))

